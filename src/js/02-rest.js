/* 
Операція ... (rest) дозволяє зібрати групу 
незалежних елементів у нову колекцію. 
Синтаксично - це близнюк операції розподілу,
але відрізнити їх просто - розподіл - коли ...
знаходиться у правій частині операції 
присвоювання, а збирання - коли ... знаходиться 
в її лівій частині.
*/

function foo(x1, x2, x3, ...args) {
  console.log(args);
}

foo(10, 20, 30, 40, 50, 60, 70);

